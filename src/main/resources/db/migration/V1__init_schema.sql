  `status` tinyint NOT NULL DEFAULT 1 COMMENT '状态: 0禁用, 1启用',
  `create_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `update_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  `deleted` tinyint NOT NULL DEFAULT 0 COMMENT '逻辑删除: 0未删除, 1已删除',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_pasture_code` (`pasture_code`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='牧场表';

CREATE TABLE IF NOT EXISTS `milk_tank` (
  `id` bigint NOT NULL AUTO_INCREMENT COMMENT '主键ID',
  `tank_code` varchar(50) NOT NULL COMMENT '奶罐编号',
  `tank_name` varchar(100) COMMENT '奶罐名称',
  `capacity` decimal(10,2) NOT NULL COMMENT '容量(升)',
  `current_volume` decimal(10,2) NOT NULL DEFAULT 0 COMMENT '当前存量(升)',
  `tank_status` varchar(20) NOT NULL DEFAULT 'IDLE' COMMENT '状态: IDLE空闲, LOADING接奶中, PENDING_TEST待检测, TESTED检测完成, LOADED已装车, PENDING_CLEAN待清洗',
  `last_clean_time` datetime COMMENT '上次清洗时间',
  `current_batch_id` bigint COMMENT '当前批次ID',
  `pasture_id` bigint NOT NULL COMMENT '所属牧场ID',
  `remark` varchar(255) COMMENT '备注',
  `create_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `update_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  `deleted` tinyint NOT NULL DEFAULT 0 COMMENT '逻辑删除: 0未删除, 1已删除',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_tank_code` (`tank_code`),
  KEY `idx_pasture` (`pasture_id`),
  KEY `idx_status` (`tank_status`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='奶罐表';

CREATE TABLE IF NOT EXISTS `milk_batch` (
  `id` bigint NOT NULL AUTO_INCREMENT COMMENT '主键ID',
  `batch_no` varchar(50) NOT NULL COMMENT '批次号',
  `pasture_id` bigint NOT NULL COMMENT '牧场ID',
  `tank_id` bigint NOT NULL COMMENT '奶罐ID',
  `milking_start_time` datetime COMMENT '挤奶开始时间',
