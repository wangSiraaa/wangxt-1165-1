  `milker_id` bigint COMMENT '挤奶员ID',
  `batch_status` varchar(20) NOT NULL DEFAULT 'MILKING' COMMENT '状态: MILKING挤奶中, PENDING_TEST待检测, TEST_PASSED检测通过, TEST_FAILED检测不合格, LOADED已装车, REJECTED已拒收',
  `remark` varchar(255) COMMENT '备注',
  `create_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `update_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  `deleted` tinyint NOT NULL DEFAULT 0 COMMENT '逻辑删除: 0未删除, 1已删除',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_batch_no` (`batch_no`),
  KEY `idx_pasture` (`pasture_id`),
  KEY `idx_tank` (`tank_id`),
  KEY `idx_status` (`batch_status`)
