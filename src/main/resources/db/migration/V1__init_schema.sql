CREATE TABLE IF NOT EXISTS `sys_user` (
  `id` bigint NOT NULL AUTO_INCREMENT COMMENT '主键ID',
  `username` varchar(50) NOT NULL COMMENT '用户名',
  `password` varchar(100) NOT NULL COMMENT '密码',
  `real_name` varchar(50) NOT NULL COMMENT '真实姓名',
  `phone` varchar(20) COMMENT '手机号',
  `role_code` varchar(20) NOT NULL COMMENT '角色编码: PASTURE牧场, LAB化验员, DRIVER司机, ADMIN管理员, LAB_SUPERVISOR化验室主管, QUALITY_SUPERVISOR质量主管, PASTURE_SUPERVISOR牧场主管, DEPARTMENT_MANAGER部门经理, QUALITY_DIRECTOR质量总监, ON_SITE_SUPERVISOR现场主管',
  `pasture_id` bigint COMMENT '所属牧场ID',
  `status` tinyint NOT NULL DEFAULT 1 COMMENT '状态: 0禁用, 1启用',
  `create_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `update_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  `deleted` tinyint NOT NULL DEFAULT 0 COMMENT '逻辑删除: 0未删除, 1已删除',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_username` (`username`),
  KEY `idx_role` (`role_code`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='用户表';

CREATE TABLE IF NOT EXISTS `pasture` (
